#/bin/bash
DB_HOST=$1
DB_PASSWORD=$2
DB_NAME=$3
CURRENT_DATE=$(date +%Y%m%d--%H%M%S)
AWS_SECRET_KEY=$4
AWS_BUCKET_NAME=$5

mysqldump -uroot -p$DB_PASSWORD -h$DB_HOST $DB_NAME > /tmp/$DB_NAME.$CURRENT_DATE.sql && \
export AWS_ACCESS_KEY_ID=AKIA3WNQTU35JK4PBO2U && \
export AWS_SECRET_ACCESS_KEY=$AWS_SECRET_KEY && \
aws s3 cp /tmp/$DB_NAME.$CURRENT_DATE.sql s3://$AWS_BUCKET_NAME/
